"""Generate the OMEdit-on-wasm omc bridge from the generated OmcSeq ABI header.

OMEdit's OMCInterface (OpenModelicaScriptingAPIQt.cpp) calls per-function C
symbols omc_scripting_<name>(...) plus the omc_seq_*/omc_abi_* container. Natively
those are the Rust omc linked in process. On wasm omc runs in a Web Worker, so
this emits a C++ translation unit that *defines* those symbols as forwarders:
each omc_scripting_<name> packs its arguments into a JSON request {fn,args},
hands it to the worker via an EM_ASYNC_JS round-trip (Asyncify keeps the call
synchronous to OMEdit while the UI stays responsive), and unpacks the JSON reply.

Input:  OpenModelicaScriptingAPIQtABI.h (the generated ABI declarations).
Output: OpenModelicaScriptingAPIQtBridge.cpp.

The argument/return type vocabulary is small and fixed (char*/int64_t/double/int/
OmcSeq*/void), so parsing the declarations is a one-liner regex.
"""
import re
import sys

PREAMBLE = r'''// GENERATED by gen_bridge.py from OpenModelicaScriptingAPIQtABI.h — DO NOT EDIT.
//
// Main-thread (Emscripten) side of the OMEdit omc bridge: the omc_scripting_*/
// omc_seq_*/omc_abi_* C ABI that OpenModelicaScriptingAPIQt.cpp calls, forwarded
// to the omc Web Worker as JSON. See gen_bridge.py.
#include "OpenModelicaScriptingAPIQtABI.h"
#include <emscripten.h>
#include <emscripten/em_js.h>
#include <QJsonDocument>
#include <QJsonArray>
#include <QJsonObject>
#include <QJsonValue>
#include <QString>
#include <cstdlib>
#include <cstring>
#include <vector>
#include <string>

// ── OmcSeq container (matches the Rust ABI's opaque sequence) ────────────────
struct OmcVal {
  enum Kind { Str, Int, Real, Bool, Seq } kind;
  std::string s;
  int64_t i = 0;
  double r = 0;
  OmcSeq *seq = nullptr;
};
struct OmcSeq { std::vector<OmcVal> items; };

extern "C" {

OmcSeq *omc_seq_new(void) { return new OmcSeq(); }
void omc_seq_free(OmcSeq *s) {
  if (!s) return;
  for (auto &v : s->items) if (v.kind == OmcVal::Seq) omc_seq_free(v.seq);
  delete s;
}
void omc_seq_push_str(OmcSeq *s, const char *p) {
  OmcVal v; v.kind = OmcVal::Str; v.s = p ? p : ""; s->items.push_back(std::move(v));
}
void omc_seq_push_int(OmcSeq *s, int64_t x)  { OmcVal v; v.kind = OmcVal::Int;  v.i = x; s->items.push_back(v); }
void omc_seq_push_real(OmcSeq *s, double x)  { OmcVal v; v.kind = OmcVal::Real; v.r = x; s->items.push_back(v); }
void omc_seq_push_bool(OmcSeq *s, int x)     { OmcVal v; v.kind = OmcVal::Bool; v.i = x ? 1 : 0; s->items.push_back(v); }
void omc_seq_push_seq(OmcSeq *s, OmcSeq *c)  { OmcVal v; v.kind = OmcVal::Seq;  v.seq = c; s->items.push_back(v); }
size_t omc_seq_len(const OmcSeq *s) { return s ? s->items.size() : 0; }
const char *omc_seq_str(const OmcSeq *s, size_t i)  { return s->items[i].s.c_str(); }
int64_t omc_seq_int(const OmcSeq *s, size_t i)      { return s->items[i].i; }
double  omc_seq_real(const OmcSeq *s, size_t i)     { return s->items[i].r; }
int     omc_seq_bool(const OmcSeq *s, size_t i)     { return (int)s->items[i].i; }
const OmcSeq *omc_seq_seq(const OmcSeq *s, size_t i) { return s->items[i].seq; }

// ── exception channel ───────────────────────────────────────────────────────
static thread_local std::string g_exception;
static thread_local bool g_has_exception = false;
int   omc_abi_has_exception(void) { return g_has_exception ? 1 : 0; }
char *omc_abi_take_exception(void) {
  g_has_exception = false;
  return strdup(g_exception.c_str());
}
void  omc_abi_free_string(char *s) { free(s); }

} // extern "C"

// ── OmcSeq <-> QJson ────────────────────────────────────────────────────────
// A sequence is a JSON array; scalars are JSON string/number/bool. This is the
// same shape OmcSeq already is, so the mapping is structural.
static QJsonValue seqToJson(const OmcSeq *s) {
  QJsonArray a;
  if (s) for (const auto &v : s->items) {
    switch (v.kind) {
      case OmcVal::Str:  a.append(QString::fromStdString(v.s)); break;
      case OmcVal::Int:  a.append((double)v.i); break;
      case OmcVal::Real: a.append(v.r); break;
      case OmcVal::Bool: a.append((bool)v.i); break;
      case OmcVal::Seq:  a.append(seqToJson(v.seq)); break;
    }
  }
  return a;
}
static OmcSeq *seqFromJson(const QJsonValue &val) {
  OmcSeq *s = new OmcSeq();
  if (val.isArray()) for (const QJsonValue &e : val.toArray()) {
    OmcVal v;
    if (e.isString())      { v.kind = OmcVal::Str;  v.s = e.toString().toStdString(); }
    else if (e.isBool())   { v.kind = OmcVal::Bool; v.i = e.toBool() ? 1 : 0; }
    else if (e.isArray())  { v.kind = OmcVal::Seq;  v.seq = seqFromJson(e); }
    else                   { v.kind = OmcVal::Real; v.r = e.toDouble(); v.i = (int64_t)e.toDouble(); }
    s->items.push_back(std::move(v));
  }
  return s;
}

// The omc worker round-trip, shared with OMCProxy.cpp's string path: post the
// request without suspending (omedit_post_abi), wait for the reply without
// suspending mid-event (omcWorkerWaitReply spins a nested QEventLoop once Qt's
// main loop runs, a raw Asyncify poll before it), then take the reply string.
// See OMEditLIB/OMC/OMCProxy.cpp for why raw Asyncify mid-event is unsafe here.
extern "C" {
  int   omedit_post_abi(const char *req);
  char *omedit_take_abi_result(int id);
}
void omcWorkerWaitReply(int id);

// Build {fn,args}, call the worker, decode the reply envelope
// {result?, error?}. Records any error on the omc_abi exception channel.
static QJsonValue omc_bridge_call(const char *fn, const QJsonArray &args) {
  QJsonObject req;
  req["fn"] = QString::fromUtf8(fn);
  req["args"] = args;
  QByteArray reqBytes = QJsonDocument(req).toJson(QJsonDocument::Compact);
  int id = omedit_post_abi(reqBytes.constData());
  omcWorkerWaitReply(id);
  char *respC = omedit_take_abi_result(id);
  QByteArray respBytes(respC);
  free(respC);
  QJsonObject resp = QJsonDocument::fromJson(respBytes).object();
  if (resp.contains("error") && !resp["error"].isNull()) {
    g_exception = resp["error"].toString().toStdString();
    g_has_exception = true;
    return QJsonValue();
  }
  return resp.value("result");
}

static char *dupString(const QJsonValue &v) {
  return strdup(v.toString().toUtf8().constData());
}
'''

# C type -> (how to append a positional arg p<i> to a QJsonArray,
#            how to turn the JSON result into the return expression)
def arg_append(ctype, var):
    if ctype == "const char*":   return f'args.append(QString::fromUtf8({var}));'
    if ctype == "int64_t":       return f'args.append((double){var});'
    if ctype == "double":        return f'args.append({var});'
    if ctype == "int":           return f'args.append((bool){var});'
    if ctype == "const OmcSeq*": return f'args.append(seqToJson({var}));'
    raise SystemExit("unknown arg type: " + ctype)

def ret_expr(ctype, val):  # val is a QJsonValue lvalue name
    if ctype == "void":     return None
    if ctype == "char*":    return f'return dupString({val});'
    if ctype == "int64_t":  return f'return (int64_t){val}.toDouble();'
    if ctype == "double":   return f'return {val}.toDouble();'
    if ctype == "int":      return f'return {val}.toBool() ? 1 : 0;'
    if ctype == "OmcSeq*":  return f'return seqFromJson({val});'
    raise SystemExit("unknown ret type: " + ctype)

DECL = re.compile(r'^\s*(char\*|int64_t|double|int|OmcSeq\*|void)\s+omc_scripting_(\w+)\s*\((.*?)\)\s*;')

def main():
    hdr, out = sys.argv[1], sys.argv[2]
    funcs = []
    for line in open(hdr):
        m = DECL.match(line)
        if not m:
            continue
        ret, name, argstr = m.group(1), m.group(2), m.group(3).strip()
        args = [] if argstr in ("", "void") else [a.strip() for a in argstr.split(",")]
        funcs.append((ret, name, args))

    body = [PREAMBLE, 'extern "C" {\n']
    for ret, name, args in funcs:
        params = ", ".join(f"{a} p{i}" for i, a in enumerate(args)) or "void"
        body.append(f"{ret} omc_scripting_{name}({params}) {{")
        body.append("  QJsonArray args;")
        for i, a in enumerate(args):
            body.append("  " + arg_append(a, f"p{i}"))
        if ret == "void":
            body.append(f'  omc_bridge_call("{name}", args);')
        else:
            body.append(f'  QJsonValue __r = omc_bridge_call("{name}", args);')
            body.append("  " + ret_expr(ret, "__r"))
        body.append("}")
    body.append("} // extern \"C\"\n")

    with open(out, "w") as f:
        f.write("\n".join(body))
    print(f"gen_bridge.py: wrote {len(funcs)} forwarders to {out}")

if __name__ == "__main__":
    main()
