/*
 * Extractor for the simulation/flag/log-stream metadata tables OMEdit reads.
 *
 * These tables (FLAG_NAME, SOLVER_METHOD_NAME, OMC_LOG_STREAM_NAME, ...) are
 * SimulationRuntime data, not part of the Rust omc port. To let OMEdit link
 * against the Rust libOpenModelicaCompiler.so WITHOUT the OpenModelica C runtime
 * (and its transitive Boehm GC), this program links the C runtime, reads the
 * tables, and emits them as Rust `#[no_mangle]` statics with the same C layout
 * (`const char* NAME[N]`). Run via gen-sim-tables.sh; output -> src/sim_metadata.rs.
 *
 * Regenerate whenever the upstream tables change; nothing here is hand-edited.
 */
#include <stdio.h>
#include "util/simulation_options.h"
#include "util/omc_error.h"

/* Emit one `const char* NAME[n]` array as a Rust CStrTable<n> static. */
static void emit(const char *name, const char *const *arr, int n)
{
  printf("#[unsafe(no_mangle)]\npub static %s: CStrTable<%d> = CStrTable([\n", name, n);
  for (int i = 0; i < n; i++) {
    if (!arr[i]) {
      printf("    core::ptr::null(),\n");
      continue;
    }
    fputs("    c\"", stdout);
    for (const unsigned char *p = (const unsigned char *) arr[i]; *p; p++) {
      unsigned char c = *p;
      if (c == '"' || c == '\\') {
        printf("\\%c", c);
      } else if (c >= 0x20 && c < 0x7f) {
        putchar(c);
      } else {
        printf("\\x%02x", c);
      }
    }
    fputs("\".as_ptr(),\n", stdout);
  }
  printf("]);\n\n");
}

int main(void)
{
  printf("// @generated by libopenmodelica_compiler/gen/gen-sim-tables.sh — do not edit.\n");
  printf("//\n");
  printf("// Simulation/flag/log-stream metadata tables OMEdit reads, mirrored from the\n");
  printf("// OpenModelica C runtime (SimulationRuntime) so OMEdit can link the Rust\n");
  printf("// libOpenModelicaCompiler.so without the C runtime / Boehm GC. Regenerate with\n");
  printf("// that script when the upstream tables change.\n");
  printf("#![allow(non_upper_case_globals)]\n");
  printf("use core::ffi::c_char;\n\n");
  printf("// `const char* NAME[N]` in C: an array of pointers to NUL-terminated strings.\n");
  printf("// Wrapped so the `static` is `Sync` (raw pointers are not); `repr(transparent)`\n");
  printf("// keeps the symbol's layout identical to the C array.\n");
  printf("#[repr(transparent)]\npub struct CStrTable<const N: usize>(pub [*const c_char; N]);\n");
  printf("// SAFETY: the pointers reference immutable, 'static c-string literals.\n");
  printf("unsafe impl<const N: usize> Sync for CStrTable<N> {}\n\n");

  emit("FLAG_NAME", FLAG_NAME, FLAG_MAX + 1);
  emit("FLAG_DETAILED_DESC", FLAG_DETAILED_DESC, FLAG_MAX + 1);
  emit("SOLVER_METHOD_NAME", SOLVER_METHOD_NAME, S_MAX);
  emit("SOLVER_METHOD_DESC", SOLVER_METHOD_DESC, S_MAX);
  emit("INIT_METHOD_NAME", INIT_METHOD_NAME, IIM_MAX);
  emit("INIT_METHOD_DESC", INIT_METHOD_DESC, IIM_MAX);
  emit("JACOBIAN_METHOD_NAME", JACOBIAN_METHOD_NAME, JAC_MAX);
  emit("JACOBIAN_METHOD_DESC", JACOBIAN_METHOD_DESC, JAC_MAX);
  emit("LS_NAME", LS_NAME, LS_MAX);
  emit("LS_DESC", LS_DESC, LS_MAX);
  emit("NLS_NAME", NLS_NAME, NLS_MAX);
  emit("NLS_DESC", NLS_DESC, NLS_MAX);
  emit("OMC_LOG_STREAM_NAME", OMC_LOG_STREAM_NAME, OMC_SIM_LOG_MAX);
  emit("OMC_LOG_STREAM_DESC", OMC_LOG_STREAM_DESC, OMC_SIM_LOG_MAX);

  printf("#[unsafe(no_mangle)]\npub static firstOMCErrorStream: core::ffi::c_int = %d;\n",
         firstOMCErrorStream);
  return 0;
}
