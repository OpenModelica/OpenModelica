@dataclass
class StreamBinding:
    """How a component exposes one stream field at a connector, taken from the
    $fdummy call arguments (which bind each connector variable to the actual
    expression the component provides)."""
    expr:     Optional[str]   # the C arg expression bound to this connector var
    varidx:   Optional[int]   # realVars index, if the expr is a plain realVar
    writable: bool            # varidx exists AND is not a computed output


def _stream_binding(comp: ComponentInfo, connector: str,
                    field: str) -> Optional[StreamBinding]:
    """Return the StreamBinding for comp.<connector>.<field>, or None if the
    connector does not expose that stream field."""
    fr, fdef = _representative_residual(comp)
    if fdef is None:
        return None
    for i, p in enumerate(fdef.params):
        if p.connector == connector and p.field == field:
            expr = fr.arg_exprs[i] if i < len(fr.arg_exprs) else None
            if expr is None:
                return StreamBinding(None, None, False)
            m = _RE_VARIDX.search(expr)
            negated = expr.lstrip().startswith('(-')
            if m and 'realParameter' not in expr and not negated:
                idx = int(m.group(1))
                # A free inStream slot must not be a computed output, a state, or
                # a state derivative — those are owned by the component/integrator
                # and overwriting them would corrupt the model.
                writable = (idx not in comp.assigned_vars
                            and idx not in comp.state_vars)
                return StreamBinding(expr, idx, writable)
            return StreamBinding(expr, None, False)   # constant / param / negated
    return None


def _stream_fields_of(comp: ComponentInfo, connector: str) -> List[str]:
    """Stream connector variables (h_outflow, Xi_outflow[...], C_outflow[...])
    exposed by *connector*, in $fdummy parameter order."""
    _, fdef = _representative_residual(comp)
    if fdef is None:
        return []
    out: List[str] = []
    for p in fdef.params:
        if p.connector == connector and classify_connector_variable(p.field) == 'stream':
            if p.field not in out:
                out.append(p.field)
    return out


def generate_stream_propagation_func(comps, edges) -> Tuple[str, List[str]]:
    """Emit combined_applyStreamVars() implementing 2-port inStream semantics,
    and return (function_text, report_lines).

    For a 2-port node, inStream(A.port.h) = B.port.h (and vice versa).  We can
    only *inject* a neighbour's value when the receiving side exposes a writable
    stream variable that the component does NOT compute itself (a free inStream
    slot).  Stream variables that OMC constant-folded to a boundary value, or
    bound to a component's own computed outflow, have no such slot, so they are
    reported but left untouched (overwriting them would corrupt the model)."""
    body: List[str] = [
        '/* Apply 2-port inStream semantics for stream (enthalpy/Xi/C) vars. */',
        '/* Call once before each component DAE evaluation pass.             */',
        'void combined_applyStreamVars(void)',
        '{',
    ]
    report: List[str] = []
    n_emitted = 0

    def dst_lhs(pfx: str, idx: int) -> str:
        return (f'  g_{pfx}_data->localData[0]->realVars'
                f'[g_{pfx}_data->simulationInfo->realVarsIndex[{idx}]]')

    def src_rhs(pfx: str, b: StreamBinding) -> str:
        # translate the provider's own expression onto its global DATA pointer
        return b.expr.replace('data->', f'g_{pfx}_data->')

    for edge in edges:
        if edge.is_signal:
            continue
        ca, cb = comps.get(edge.comp_a), comps.get(edge.comp_b)
        if ca is None or cb is None:
            continue

        fields = []
        for f in _stream_fields_of(ca, edge.conn_a):
            if f not in fields:
                fields.append(f)
        for f in _stream_fields_of(cb, edge.conn_b):
            if f not in fields:
                fields.append(f)

        for fld in fields:
            ba = _stream_binding(ca, edge.conn_a, fld)
            bb = _stream_binding(cb, edge.conn_b, fld)
            if ba is None or bb is None:
                continue
            tag = f'connect({edge.comp_a}.{edge.conn_a}, {edge.comp_b}.{edge.conn_b}).{fld}'
            body.append(f'  /* {tag} */')

            # inStream(A) = B.outflow  -> write A's slot if it is a free inStream var
            if ba.writable and bb.expr is not None:
                body.append(f'  /* inStream({edge.comp_a}.{edge.conn_a}.{fld})'
                            f' = {edge.comp_b}.{edge.conn_b}.{fld} */')
                body.append(dst_lhs(edge.comp_a, ba.varidx) + ' =')
                body.append('    ' + src_rhs(edge.comp_b, bb) + ';')
                n_emitted += 1
                report.append(f'  {tag}: -> {edge.comp_a} (writable inStream slot)')
            # inStream(B) = A.outflow
            if bb.writable and ba.expr is not None:
                body.append(f'  /* inStream({edge.comp_b}.{edge.conn_b}.{fld})'
                            f' = {edge.comp_a}.{edge.conn_a}.{fld} */')
                body.append(dst_lhs(edge.comp_b, bb.varidx) + ' =')
                body.append('    ' + src_rhs(edge.comp_a, ba) + ';')
                n_emitted += 1
                report.append(f'  {tag}: -> {edge.comp_b} (writable inStream slot)')

            if not ba.writable and not bb.writable:
                def descr(b):
                    if b.varidx is not None:
                        return f'component-owned rv{b.varidx}'   # state or output
                    return f'boundary/constant ({(b.expr or "?").strip()[:24]})'
                body.append(f'  /* no free inStream slot: '
                            f'{edge.comp_a}={descr(ba)}, {edge.comp_b}={descr(bb)} '
                            f'-> nothing to propagate */')
                report.append(f'  {tag}: degenerate ({descr(ba)} / {descr(bb)})')

    if n_emitted == 0:
        body.insert(4, '  /* No writable inStream slots in this configuration:')
        body.insert(5, '     all stream variables are boundary-constant-folded or')
        body.insert(6, '     are components\' own computed outflows (see report). */')
    body.append('}')
    return '\n'.join(body), report


def generate_signal_propagation_func(comps, edges, raw_connections) -> str:
    lines = [
        '/* Copy signal (Real output->input) connections.       */',
        '/* Call after evaluating the source component.         */',
        'void combined_applySignalConnections(void)',
        '{',
    ]
    for ca, pna, cb, pnb in raw_connections:
        comp_a = comps.get(ca)
        comp_b = comps.get(cb)
        if comp_a is None or comp_b is None:
            continue

        idx_a_var   = comp_a.var_to_idx.get(pna)
        idx_a_param = comp_a.param_to_idx.get(pna)
        idx_b_var   = comp_b.var_to_idx.get(pnb)
        idx_b_param = comp_b.param_to_idx.get(pnb)

        if (idx_a_var is not None or idx_a_param is not None) and \
           (idx_b_var is not None or idx_b_param is not None):
            if idx_a_var is not None:
                src = (f'g_{ca}_data->localData[0]->realVars'
                       f'[g_{ca}_data->simulationInfo->realVarsIndex[{idx_a_var}]]'
                       f' /* {ca}.{pna} */')
            else:
                src = (f'g_{ca}_data->simulationInfo->realParameter'
                       f'[g_{ca}_data->simulationInfo->realParamsIndex[{idx_a_param}]]'
                       f' /* {ca}.{pna} PARAM */')
            if idx_b_var is not None:
                dst = (f'g_{cb}_data->localData[0]->realVars'
                       f'[g_{cb}_data->simulationInfo->realVarsIndex[{idx_b_var}]]'
                       f' /* {cb}.{pnb} */')
            else:
                dst = (f'g_{cb}_data->simulationInfo->realParameter'
                       f'[g_{cb}_data->simulationInfo->realParamsIndex[{idx_b_param}]]'
                       f' /* {cb}.{pnb} PARAM */')
            lines.append(f'  /* connect({ca}.{pna}, {cb}.{pnb}) */')
            lines.append(f'  {dst} = {src};')

    lines.append('}')
    return '\n'.join(lines)


