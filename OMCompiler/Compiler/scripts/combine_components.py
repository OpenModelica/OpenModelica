# Data structures
# ---------------------------------------------------------------------------

@dataclass
class FdummyParam:
    """One parameter of a $fdummyN function, decoded to Modelica names."""
    c_name:      str    # original C parameter name
    model_name:  str    # decoded Modelica name  e.g. 'port_a.m_flow'
    connector:   str    # connector portion       e.g. 'port_a'
    field:       str    # field portion           e.g. 'm_flow'
    kind:        str    # 'flow' | 'potential' | 'stream' | 'signal'


@dataclass
class FdummyDef:
    """One $fdummyN function and its decoded parameter list."""
    number:  int                 # 1, 2, 3, ...
    params:  List[FdummyParam]   # ordered list of parameters
    connectors: List[str] = field(default_factory=list)

    def __post_init__(self):
        seen: List[str] = []
        for p in self.params:
            if p.connector and p.connector not in seen:
                seen.append(p.connector)
        self.connectors = seen


@dataclass
class FdummyResidual:
    """One DAE residual that is a $fdummyN call (placeholder for a connect eq)."""
    residual_idx:  int         # N in $DAEresN
    fdummy_num:    int         # M in $fdummyM
    eq_func_name:  str         # 'pfx_eqFunction_K'
    eq_func_num:   int         # K
    arg_exprs:     List[str]   # C expressions passed as call arguments, in order
    arg_comments:  List[str]   # variable names from /* ... variable */ comments


@dataclass
class ComponentInfo:
    prefix:    str
    directory: str

    alg_indexes: List[int] = field(default_factory=list)
    n_residuals: int = 0
    n_alg:       int = 0          # nAlgebraicDAEVars

    fdummy_defs: Dict[int, FdummyDef] = field(default_factory=dict)
    fdummy_residuals: List[FdummyResidual] = field(default_factory=list)

    var_to_idx:   Dict[str, int] = field(default_factory=dict)
    param_to_idx: Dict[str, int] = field(default_factory=dict)

    # realVars indices the component assigns itself (outputs of its equations).
    # Used to avoid clobbering a computed output when propagating stream values.
    assigned_vars: Set[int] = field(default_factory=set)
    # realVars indices that are states or state derivatives (integrator-owned).
    state_vars: Set[int] = field(default_factory=set)

    # DAE-mode sparse pattern (dResiduals/dUnknowns), CSC.
    # col_rows[j] = sorted list of residual rows that depend on unknown column j.
    # Column convention: columns 0..n_states-1 are states (realVars 0..n_states-1),
    # the remaining columns are the algebraic vars in alg_indexes order.
    col_rows: List[List[int]] = field(default_factory=list)

    @property
    def n_states(self) -> int:
        return self.n_residuals - self.n_alg

    def realvar_to_col(self, k: int) -> Optional[int]:
        """Map a realVars index to its DAE-unknown column, or None if the
        variable is neither a state nor an algebraic DAE unknown (an
        intermediate computed inside the residual evaluation)."""
        if k < self.n_states:
            return k
        if k in self.alg_indexes:
            return self.n_states + self.alg_indexes.index(k)
        return None


# ---------------------------------------------------------------------------
# Parsing helpers
# ---------------------------------------------------------------------------

# realVars[data->simulationInfo->realVarsIndex[N]] /* name variable */
# Note the double ]] : one closes realVarsIndex[N], one closes realVars[...].
_RE_REALVAR  = re.compile(
    r'realVarsIndex\[(\d+)\]\]\s*/\*\s*([^\*]+?)\s*'
    r'(variable|STATE|STATE_DER|DAE_RESIDUAL|DAE_AUX)\s*\*/')
_RE_PARAM    = re.compile(
    r'realParamsIndex\[(\d+)\]\]\s*/\*\s*([^\*]+?)\s*PARAM\s*\*/')

# The connector placeholder function is named omc__omcQ_24fdummyN by OMC <= 1.27
# and omc_fN by OMC >= 1.28; accept either spelling.
_FDUMMY_CFUNC = r'omc_(?:_omcQ_24fdummy|f)(\d+)'
_RE_FDUMMY_CALL = re.compile(
    r'\(data->simulationInfo->daeModeData->residualVars\[(\d+)\]\).*?='
    r'\s*' + _FDUMMY_CFUNC + r'\s*\(threadData,\s*(.+?)\);',
    re.DOTALL
)
_RE_EQFUNC_START = re.compile(
    r'void\s+(\w+)_eqFunction_(\d+)\s*\(DATA\s*\*data,\s*threadData_t\s*\*threadData\)')

_RE_ALG_INDEXES = re.compile(
    r'const int algIndexes\[(\d+)\]\s*=\s*\{([^}]+)\}')
_RE_NRESIDUALS  = re.compile(
    r'daeModeData->nResidualVars\s*=\s*(\d+)\s*;')
_RE_NALG        = re.compile(
    r'daeModeData->nAlgebraicDAEVars\s*=\s*(\d+)\s*;')
_RE_COLPTR      = re.compile(
    r'const int colPtrIndex\[1\+\d+\]\s*=\s*\{([^}]+)\}')
_RE_ROWINDEX    = re.compile(
    r'const int rowIndex\[\d+\]\s*=\s*\{([^}]+)\}')

_RE_FDUMMY_DECL = re.compile(
    r'modelica_real\s+' + _FDUMMY_CFUNC + r'\s*\('
    r'threadData_t\s*\*threadData\s*,\s*(.+?)\);',
    re.DOTALL
)
_RE_FDUMMY_PARAM_NAME = re.compile(r'modelica_real\s+(__omcQ\w+|\w+)')

_RE_CONNECT = re.compile(
    r'connect\s*\(\s*(\w+)\s*\.\s*([\w\[\]]+)\s*,\s*(\w+)\s*\.\s*([\w\[\]]+)\s*\)')

_RE_VARIDX = re.compile(r'realVarsIndex\[(\d+)\]')

# A realVars reference that is the left-hand side of an assignment (i.e. a value
# the component computes itself) looks like:  realVars[...realVarsIndex[N]] /* x */) =
_RE_ASSIGN_LHS = re.compile(
    r'realVars\[data->simulationInfo->realVarsIndex\[(\d+)\]\]\s*'
    r'/\*[^*]*\*/\s*\)\s*=(?!=)')

# realVarsIndex[N]] /* name STATE(1) */  or  /* der(name) STATE_DER */
_RE_STATE_VAR = re.compile(
    r'realVarsIndex\[(\d+)\]\]\s*/\*[^*]*\bSTATE(?:_DER)?\b')


def _extract_c_args(arg_str: str) -> List[str]:
    """Split a C call-argument string respecting nested parentheses."""
    args, depth, current = [], 0, []
    for ch in arg_str:
        if ch == '(':
            depth += 1
            current.append(ch)
        elif ch == ')':
            depth -= 1
            current.append(ch)
        elif ch == ',' and depth == 0:
            args.append(''.join(current).strip())
            current = []
        else:
            current.append(ch)
    if current:
        args.append(''.join(current).strip())
    return args


def _comment_of(expr: str) -> str:
    """Extract variable name from a /* ... variable */ comment in a C expression."""
    m = re.search(r'/\*\s*(.*?)\s*(?:variable|STATE|STATE_DER|PARAM)\s*\*/', expr)
    return m.group(1).strip() if m else expr.strip()


def parse_functions_file(path: str) -> Dict[int, FdummyDef]:
    """Parse xxx_functions.h to extract $fdummyN parameter names."""
    text = open(path).read()
    defs: Dict[int, FdummyDef] = {}

    for m in _RE_FDUMMY_DECL.finditer(text):
        num      = int(m.group(1))
        params_s = m.group(2)
        params: List[FdummyParam] = []
        for pm in _RE_FDUMMY_PARAM_NAME.finditer(params_s):
            c_name = pm.group(1)
            if c_name == 'threadData_t':
                continue
            model = decode_omc_ident(c_name)
            conn  = connector_of(model)
            fld   = model[len(conn) + 1:] if conn else model
            kind  = classify_connector_variable(fld)
            params.append(FdummyParam(c_name, model, conn, fld, kind))
        if params:
            defs[num] = FdummyDef(number=num, params=params)

    return defs


def _parse_sparse_pattern(text: str, n_res: int) -> List[List[int]]:
    """Parse colPtrIndex/rowIndex from initializeDAEmodeData into per-column
    row lists.  The stored colPtrIndex is the un-accumulated form OM expands at
    runtime (leadindex[i] += leadindex[i-1] for i>=2), so the accumulated
    boundaries are the running sum of colPtrIndex[1:]."""
    cp_m = _RE_COLPTR.search(text)
    ri_m = _RE_ROWINDEX.search(text)
    if not cp_m or not ri_m or n_res <= 0:
        return [[] for _ in range(n_res)]
    raw_cp = [int(x.strip()) for x in cp_m.group(1).split(',')]
    rows   = [int(x.strip()) for x in ri_m.group(1).split(',')]
    # accumulated[i] = sum(raw_cp[1..i]); accumulated[0] = 0
    acc = [0]
    for v in raw_cp[1:]:
        acc.append(acc[-1] + v)
    col_rows: List[List[int]] = []
    for j in range(n_res):
        if j + 1 < len(acc):
            col_rows.append(sorted(rows[acc[j]:acc[j + 1]]))
        else:
            col_rows.append([])
    return col_rows


def parse_dae_file(path: str, prefix: str) -> Tuple[
        List[FdummyResidual], Dict[str, int], Dict[str, int],
        int, List[int], int, List[List[int]], Set[int], Set[int]]:
    """Parse xxx_16dae.c for fdummy residuals, var/param index maps, residual
    count, algebraic variable indices, nAlgebraicDAEVars, the DAE-mode sparse
    pattern (per-column residual-row lists) and the set of realVars indices the
    component assigns itself."""
    text = open(path).read()
    assigned_vars = {int(m.group(1)) for m in _RE_ASSIGN_LHS.finditer(text)}

    alg_m = _RE_ALG_INDEXES.search(text)
    alg_indexes = [int(x.strip()) for x in alg_m.group(2).split(',')] if alg_m else []
    nres_m = _RE_NRESIDUALS.search(text)
    n_res  = int(nres_m.group(1)) if nres_m else 0
    nalg_m = _RE_NALG.search(text)
    n_alg  = int(nalg_m.group(1)) if nalg_m else len(alg_indexes)
    col_rows = _parse_sparse_pattern(text, n_res)

    var_to_idx: Dict[str, int] = {}
    for vm in _RE_REALVAR.finditer(text):
        idx, raw_name = int(vm.group(1)), vm.group(2).strip()
        name = raw_name.split(' ')[0]
        var_to_idx.setdefault(name, idx)

    # states/derivatives are tagged STATE(...) or STATE_DER in their comments;
    # the comment may carry a (..) suffix, so match leniently on the word.
    state_vars = {int(m.group(1)) for m in _RE_STATE_VAR.finditer(text)}

    param_to_idx: Dict[str, int] = {}
    for pm in _RE_PARAM.finditer(text):
        idx, raw_name = int(pm.group(1)), pm.group(2).strip()
        name = raw_name.split(' ')[0]
        param_to_idx.setdefault(name, idx)

    fdummy_residuals: List[FdummyResidual] = []
    func_starts = list(_RE_EQFUNC_START.finditer(text))
    for i, fs in enumerate(func_starts):
        func_num   = int(fs.group(2))
        body_start = fs.end()
        body_end   = func_starts[i + 1].start() if i + 1 < len(func_starts) else len(text)
        body       = text[body_start:body_end]

        m = _RE_FDUMMY_CALL.search(body)
        if not m:
            continue

        res_idx    = int(m.group(1))
        fdummy_num = int(m.group(2))
        arg_exprs  = _extract_c_args(m.group(3))
        fdummy_residuals.append(FdummyResidual(
            residual_idx = res_idx,
            fdummy_num   = fdummy_num,
            eq_func_name = f'{prefix}_eqFunction_{func_num}',
            eq_func_num  = func_num,
            arg_exprs    = arg_exprs,
            arg_comments = [_comment_of(e) for e in arg_exprs],
        ))

    return (fdummy_residuals, var_to_idx, param_to_idx,
            n_res, alg_indexes, n_alg, col_rows, assigned_vars, state_vars)


def parse_connections(path: str) -> List[Tuple[str, str, str, str]]:
    """Parse connect() statements -> list of (comp_a, port_a, comp_b, port_b)."""
    text = open(path).read()
    return [(m.group(1), m.group(2), m.group(3), m.group(4))
            for m in _RE_CONNECT.finditer(text)]


def load_component(prefix: str, directory: str) -> ComponentInfo:
    comp = ComponentInfo(prefix=prefix, directory=directory)

    funcs_h = os.path.join(directory, f'{prefix}_functions.h')
    dae_c   = os.path.join(directory, f'{prefix}_16dae.c')

    if os.path.exists(funcs_h):
        comp.fdummy_defs = parse_functions_file(funcs_h)
    if os.path.exists(dae_c):
        (comp.fdummy_residuals, comp.var_to_idx, comp.param_to_idx,
         comp.n_residuals, comp.alg_indexes, comp.n_alg, comp.col_rows,
         comp.assigned_vars, comp.state_vars) = parse_dae_file(dae_c, prefix)

    return comp


