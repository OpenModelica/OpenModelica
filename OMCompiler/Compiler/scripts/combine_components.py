# ---------------------------------------------------------------------------
# OMC C-identifier -> Modelica name decoder
# ---------------------------------------------------------------------------

def decode_omc_ident(ident: str) -> str:
    """
    Decode an OpenModelica-mangled C identifier to a Modelica variable name.

    Encoding rules used by OMC:
      * A non-ASCII or special character with code X is written as _XX (two hex digits).
      * Quoted Modelica identifiers are wrapped in single-quote characters (code 0x27).
      * The prefix __omcQ is prepended when the source name was a quoted identifier.

    Examples:
      __omcQ_27ports_5B1_5D_2Em_5Fflow_27        ->  ports[1].m_flow
      __omcQ_27port_5Fa_2Ep_27                   ->  port_a.p
      __omcQ_27heatPort_2EQ_5Fflow_27            ->  heatPort.Q_flow
      __omcQ_27ports_5B1_5D_2EXi_5Foutflow_27_5B1_5D  ->  ports[1].Xi_outflow[1]
    """
    s = ident
    if s.startswith('__omcQ'):
        s = s[len('__omcQ'):]

    result = []
    i = 0
    while i < len(s):
        if s[i] == '_' and i + 2 < len(s) and all(
                c in '0123456789abcdefABCDEF' for c in s[i + 1:i + 3]):
            result.append(chr(int(s[i + 1:i + 3], 16)))
            i += 3
        else:
            result.append(s[i])
            i += 1

    name = ''.join(result)
    # Remove ALL single-quote characters (OMC uses them as quoted-name delimiters,
    # but in variable names they are just noise:
    #   'ports[1].Xi_outflow'[1] -> ports[1].Xi_outflow[1])
    name = name.replace("'", "")
    return name


# ---------------------------------------------------------------------------
# Connector-variable classification (FluidPort / HeatPort / Signal)
# ---------------------------------------------------------------------------

FLOW_VARS      = {'m_flow', 'Q_flow'}
POTENTIAL_VARS = {'p', 'T'}
STREAM_VARS_RE = re.compile(r'^(h_outflow|Xi_outflow|C_outflow)')


def classify_connector_variable(field_name: str) -> str:
    """Return 'flow', 'potential', 'stream', or 'signal'."""
    base = field_name.split('[')[0]          # strip array index
    if base in FLOW_VARS:
        return 'flow'
    if base in POTENTIAL_VARS:
        return 'potential'
    if STREAM_VARS_RE.match(base):
        return 'stream'
    return 'signal'


def connector_of(var_name: str) -> str:
    """Extract the connector name from a dotted variable name.

    'port_a.m_flow'    -> 'port_a'
    'ports[1].p'       -> 'ports[1]'
    'heatPort.Q_flow'  -> 'heatPort'
    'y'                -> ''          (scalar signal - no sub-connector)
    """
    dot = var_name.find('.')
    if dot < 0:
        return ''
    return var_name[:dot]


