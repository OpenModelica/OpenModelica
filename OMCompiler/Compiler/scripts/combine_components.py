def emit_conn_eq_func(comp: ComponentInfo, fr: FdummyResidual,
                      connector: Optional[str], spec: Optional[ConnEq],
                      comps) -> str:
    """Generate the replacement eqFunction for one $fdummy residual."""
    prefix = comp.prefix
    residual_var = (f'(data->simulationInfo->daeModeData->residualVars'
                    f'[{fr.residual_idx}])')

    L: List[str] = []
    L.append(f'/* Replacement for {prefix}_eqFunction_{fr.eq_func_num}')
    L.append(f' * Original: $DAEres{fr.residual_idx} = '
             f'$fdummy{fr.fdummy_num}(...) = 0')

    # --- unconnected port: open => flow variable = 0 ---------------------
    if spec is None or not spec.connected:
        L.append(f' * {prefix}.{connector}: not connected -> flow = 0 */')
        L.append('OMC_DISABLE_OPT')
        L.append(f'void {prefix}_eqFunction_{fr.eq_func_num}'
                 f'(DATA *data, threadData_t *threadData)')
        L.append('{')
        flow_expr = (_member_field_expr((prefix, connector), spec.flow_field,
                                        prefix, comps)
                     if spec else None)
        if flow_expr:
            L.append(f'  /* open port {prefix}.{connector}: flow = 0 */')
            L.append(f'  {residual_var} = {flow_expr};')
        else:
            L.append(f'  {residual_var} = 0.0;')
        L.append(f'  threadData->lastEquationSolved = {fr.eq_func_num};')
        L.append('}')
        return '\n'.join(L)

    members = spec.members

    if spec.kind == 'flow':
        node_str = ', '.join(f'{c}.{n}' for c, n in members)
        L.append(f' * Flow conservation at node {{{node_str}}} */')
        L.append('OMC_DISABLE_OPT')
        L.append(f'void {prefix}_eqFunction_{fr.eq_func_num}'
                 f'(DATA *data, threadData_t *threadData)')
        L.append('{')
        terms = [(_member_field_expr(m, spec.flow_field, prefix, comps)
                  or '0.0 /* unresolved */') for m in members]
        L.append(f'  /* sum of connector flows = 0 */')
        L.append(f'  {residual_var} =')
        L.append('      ' + '\n    + '.join(terms) + ';')
        L.append(f'  threadData->lastEquationSolved = {fr.eq_func_num};')
        L.append('}')
    else:  # potential equality
        owner   = spec.owner
        my_p    = _member_field_expr((prefix, connector), spec.pot_field,
                                     prefix, comps)
        owner_p = _member_field_expr(owner, spec.pot_field, prefix, comps)
        L.append(f' * Potential equality: {prefix}.{connector}.{spec.pot_field}'
                 f' = {owner[0]}.{owner[1]}.{spec.pot_field} */')
        L.append('OMC_DISABLE_OPT')
        L.append(f'void {prefix}_eqFunction_{fr.eq_func_num}'
                 f'(DATA *data, threadData_t *threadData)')
        L.append('{')
        if my_p and owner_p:
            L.append(f'  {residual_var} =')
            L.append(f'      {my_p}')
            L.append(f'    - {owner_p};')
        else:
            L.append(f'  /* WARNING unresolved potential: '
                     f'my={my_p} owner={owner_p} */')
            L.append(f'  {residual_var} = 0.0;')
        L.append(f'  threadData->lastEquationSolved = {fr.eq_func_num};')
        L.append('}')

    return '\n'.join(L)


def _find_eqfunction_span(text: str, prefix: str, k: int) -> Optional[Tuple[int, int]]:
    """Return the [start, end) byte span of the eqFunction_<k> definition in
    *text*, including a leading `OMC_DISABLE_OPT` line if present."""
    sig = f'void {prefix}_eqFunction_{k}(DATA *data, threadData_t *threadData)'
    pos = text.find(sig)
    if pos < 0:
        return None
    # absorb an immediately-preceding OMC_DISABLE_OPT line into the span
    line_start = text.rfind('\n', 0, pos) + 1
    prev_nl    = (text.rfind('\n', 0, line_start - 1) + 1) if line_start > 0 else 0
    start = prev_nl if text[prev_nl:line_start].strip() == 'OMC_DISABLE_OPT' else pos
    # brace-match the function body
    brace = text.find('{', pos)
    if brace < 0:
        return None
    depth, i = 0, brace
    while i < len(text):
        if text[i] == '{':
            depth += 1
        elif text[i] == '}':
            depth -= 1
            if depth == 0:
                return (start, i + 1)
        i += 1
    return None


_RE_INSTREAM_CALL = re.compile(r'inStream\(\(([^()]*)\)\)')
_RE_VAR_COMMENT   = re.compile(r'/\*\s*([\w.\[\]]+)\s+variable\s*\*/')


def _neighbor_of(edges, pfx: str, conn: str):
    """Return (neighbor_prefix, neighbor_connector) for component pfx's connector
    conn, following the fluid connection edges, or None if unconnected."""
    for e in edges:
        if e.is_signal:
            continue
        if e.comp_a == pfx and e.conn_a == conn:
            return (e.comp_b, e.conn_b)
        if e.comp_b == pfx and e.conn_b == conn:
            return (e.comp_a, e.conn_a)
    return None


def _replace_instream_calls(text: str, comp: ComponentInfo, edges, comps) -> str:
    """Replace each deferred inStream(<comp.connector.streamfield>) call left in
    the DAE residual code (translateResidualsDAE keeps inStream unevaluated for
    unconnected stream connectors) with the value flowing in from the connected
    neighbour, i.e. the neighbour connector's outflow on its global DATA pointer.
    This is the stream-connector analogue of the $fdummy flow/potential
    replacement: it resolves inStream once the components are coupled."""
    report: List[str] = []

    def repl(m):
        inner = m.group(1)
        vm = _RE_VAR_COMMENT.search(inner)
        if not vm:
            return m.group(0)
        full = vm.group(1)                 # e.g. port_b.h_outflow or ports[1].Xi_outflow[1]
        dot = full.find('.')
        if dot < 0:
            return m.group(0)
        conn, field = full[:dot], full[dot + 1:]
        nb = _neighbor_of(edges, comp.prefix, conn)
        if nb is None:
            return m.group(0)
        nb_pfx, nb_conn = nb
        nb_comp = comps.get(nb_pfx)
        if nb_comp is None:
            return m.group(0)
        b = _stream_binding(nb_comp, nb_conn, field)
        if b is None or b.expr is None:
            return m.group(0)
        report.append(f'    inStream({comp.prefix}.{full}) <- {nb_pfx}.{nb_conn}.{field}')
        return '(' + b.expr.replace('data->', f'g_{nb_pfx}_data->') + ')'

    text = _RE_INSTREAM_CALL.sub(repl, text)
    if report:
        text = ('/* resolved deferred inStream() to neighbour outflow:\n'
                + '\n'.join(report) + '\n */\n' + text)
    return text


def generate_connected_dae(comp: ComponentInfo,
                           specs: Dict[Tuple[str, str], ConnEq],
                           comps, edges) -> str:
    """Generate connected_<pfx>_16dae.c: a *complete, drop-in* copy of the
    original <pfx>_16dae.c with the $fdummy placeholder eqFunctions replaced
    in place by the real connector equations.

    Emitting the whole translation unit (not just the replacements) is
    essential: the file also defines the component's physics eqFunctions,
    <pfx>_evaluateDAEResiduals and <pfx>_initializeDAEmodeData, which the
    driver/solver must link against.  connected_<pfx>_16dae.o therefore fully
    replaces <pfx>_16dae.o."""
    prefix = comp.prefix
    orig   = os.path.join(comp.directory, f'{prefix}_16dae.c')
    text   = open(orig).read()

    # make the cross-component g_<comp>_data pointers visible
    anchor = '#include "simulation/solver/dae_mode.h"'
    if 'combined_setup.h' not in text and anchor in text:
        text = text.replace(anchor, anchor + '\n#include "combined_setup.h"', 1)

    cr_map      = connector_residual_map(comp)
    res_to_conn = {fr.eq_func_num: conn for conn, fr in cr_map.items()}

    # collect replacements, then splice from last to first to keep offsets valid
    repls: List[Tuple[int, int, str]] = []
    for fr in comp.fdummy_residuals:
        connector   = res_to_conn.get(fr.eq_func_num)
        spec        = specs.get((prefix, connector)) if connector else None
        replacement = emit_conn_eq_func(comp, fr, connector, spec, comps)
        span        = _find_eqfunction_span(text, prefix, fr.eq_func_num)
        if span is None:
            replacement = (f'/* WARNING: could not locate '
                           f'{prefix}_eqFunction_{fr.eq_func_num} to override */\n'
                           + replacement)
            continue
        repls.append((span[0], span[1], replacement))

    for s, e, r in sorted(repls, reverse=True):
        text = text[:s] + r + '\n' + text[e:]

    # resolve any deferred inStream() calls to the connected neighbour outflow
    text = _replace_instream_calls(text, comp, edges, comps)

    # translateResidualsDAE keeps inStream() unevaluated for unconnected stream
    # connectors; we resolved those in the DAE residual above, but the (light-init
    # / bound) files of this component still contain inStream(<const>) calls that
    # collapse to their argument.  Provide that identity here so each component
    # library links; -Wl,-Bsymbolic binds the other objects to this definition.
    instream_def = ('\n/* identity for inStream() left in non-residual files of '
                    'this component (unconnected stream connector -> inStream(c)=c) */\n'
                    'double inStream(double __x);\n'
                    'double inStream(double __x) { return __x; }\n')

    header = (f'/* AUTO-GENERATED by combine_components.py — DO NOT EDIT.\n'
              f' * Drop-in replacement for {orig}: the $fdummy placeholder\n'
              f' * residuals are overridden with the real connector equations,\n'
              f' * deferred inStream() calls are resolved to neighbour outflows,\n'
              f' * all other (physics) eqFunctions are preserved verbatim.\n'
              f' */\n')
    return header + instream_def + text


