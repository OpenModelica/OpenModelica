# Top-level component types we translate into separate DAE components.  Anything
# else found at the top level (parameters, constants, packages, ...) is copied
# verbatim into every wrapper so the component modifications can resolve.
_TRANSLATE_RESTRICTIONS = {'model', 'block'}


def _mi_cref_to_str(cref):
    """Render a getModelInstance cref ({parts:[{name,subscripts?}]}) as 'a.b[1]'."""
    out = []
    for p in cref.get('parts', []):
        s = p['name']
        subs = p.get('subscripts')
        if subs:
            s += '[' + ','.join(str(x) for x in subs) + ']'
        out.append(s)
    return '.'.join(out)


def _mi_render_mods(mods, classmap):
    """Reconstruct a Modelica modification string '(a = 1, redeclare ...)' from
    the getModelInstance 'modifiers' object.  classmap resolves redeclare targets
    that refer to an inherited replaceable class (e.g. 'redeclare package Medium =
    Medium' -> 'redeclare package Medium = Buildings.Media.Air') so the wrapper is
    self-contained and avoids a recursive short-class definition."""
    if not mods:
        return ''
    parts = []
    for key, val in mods.items():
        if isinstance(val, str):
            parts.append(f'{key} = {val}')
            continue
        if not isinstance(val, dict):
            continue
        if '$value' in val:
            v = val['$value']
            attrs = {k: vv for k, vv in val.items() if k != '$value'}
            if isinstance(v, dict) and v.get('$kind') == 'class':
                pre = 'redeclare ' if v.get('prefixes', {}).get('redeclare') else ''
                if v.get('prefixes', {}).get('replaceable'):
                    pre += 'replaceable '
                restr = v.get('restriction', 'package')
                base = v.get('baseClass')
                base = classmap.get(base, base)
                parts.append(f'{pre}{restr} {key} = {base}')
            elif isinstance(v, dict) and v.get('$kind') == 'component':
                pre = 'redeclare ' if v.get('prefixes', {}).get('redeclare') else ''
                parts.append(f'{pre}{v.get("type")} {key}')
            elif isinstance(v, str):
                if attrs:
                    astr = ', '.join(f'{ak} = {av}' for ak, av in attrs.items())
                    parts.append(f'{key}({astr}) = {v}')
                else:
                    parts.append(f'{key} = {v}')
            else:
                inner = _mi_render_mods(v, classmap) if isinstance(v, dict) else ''
                parts.append(f'{key}{inner}')
        else:
            inner = _mi_render_mods(val, classmap)
            if inner:
                parts.append(f'{key}{inner}')
    return '(' + ', '.join(parts) + ')' if parts else ''


def _mi_collect_classes(node, classmap):
    """Recursively gather inherited class/package definitions (name -> baseClass)
    so redeclare targets that reference them can be resolved to a concrete class."""
    for e in node.get('elements', []):
        if not isinstance(e, dict):
            continue
        kind = e.get('$kind')
        if kind == 'extends' and isinstance(e.get('baseClass'), dict):
            _mi_collect_classes(e['baseClass'], classmap)
        elif kind == 'class' and e.get('name') and e.get('baseClass'):
            classmap.setdefault(e['name'], e['baseClass'])


def _mi_render_class(e):
    """Render a getModelInstance class element (e.g. an inherited
    'replaceable package Medium = Buildings.Media.Air constrainedby ...') back to
    a Modelica short-class definition, so component modifications that refer to it
    (redeclare package Medium = Medium) resolve inside the wrapper."""
    name = e.get('name')
    restr = e.get('restriction', 'package')
    base = e.get('baseClass')
    if base is None:
        return None
    pre = e.get('prefixes', {}) or {}
    s = ''
    if pre.get('final'):
        s += 'final '
    if pre.get('redeclare'):
        s += 'redeclare '
    repl = pre.get('replaceable')
    cb = None
    if repl:
        s += 'replaceable '
        if isinstance(repl, dict):
            cb = repl.get('constrainedby')
    decl = f'{s}{restr} {name} = {base}'
    if cb:
        decl += f' constrainedby {cb}'
    return decl + ';'


def _mi_collect(node, comps, copies, conns, classmap):
    """Recursively walk a model-instance node, descending through 'extends' but
    NOT into component types.  Fills comps (to translate), copies (top-level
    parameter decls to copy into wrappers) and conns (connection cref pairs)."""
    for e in node.get('elements', []):
        if not isinstance(e, dict):
            continue
        kind = e.get('$kind')
        if kind == 'extends':
            bc = e.get('baseClass')
            if isinstance(bc, dict):
                _mi_collect(bc, comps, copies, conns, classmap)
        elif kind == 'component':
            ty = e.get('type', {})
            tyname = ty.get('name') if isinstance(ty, dict) else ty
            restr = ty.get('restriction') if isinstance(ty, dict) else None
            name = e.get('name')
            mods = e.get('modifiers', {})
            if restr in _TRANSLATE_RESTRICTIONS:
                comps.append((name, tyname, _mi_render_mods(mods, classmap)))
            else:
                # parameter / constant etc. -> copy into every wrapper
                val = mods.get(name) if isinstance(mods, dict) else None
                bind = f' = {val}' if isinstance(val, str) else ''
                copies.append(f'{tyname} {name}{bind};')
    for c in node.get('connections', []):
        if isinstance(c, dict) and 'lhs' in c and 'rhs' in c:
            conns.append((_mi_cref_to_str(c['lhs']), _mi_cref_to_str(c['rhs'])))


def cmd_prepare(argv):
    ap = argparse.ArgumentParser(prog='combine_components.py prepare')
    ap.add_argument('--instance', required=True,
                    help='Path to the getModelInstance() JSON dump')
    ap.add_argument('--dir', required=True, help='Output directory')
    ap.add_argument('--model', required=True,
                    help='Top-level model name (used for _connect_<model>.txt)')
    args = ap.parse_args(argv)

    directory = os.path.abspath(args.dir)
    with open(args.instance) as f:
        inst = json.load(f)

    comps, copies, conns, classmap = [], [], [], {}
    _mi_collect_classes(inst, classmap)
    _mi_collect(inst, comps, copies, conns, classmap)

    if not comps:
        raise SystemExit('prepare: no top-level model/block components found')

    copy_block = ''.join(f'  {c}\n' for c in copies)

    # one file with all wrappers + a content signature per component so the OMC
    # orchestrator can skip re-generating a component whose top-level model has
    # the same type+modifications (and copied decls) as a previous run.
    import hashlib
    wrappers = []
    sigs = {}
    for name, tyname, mods in comps:
        body = f'{copy_block}  extends {tyname}{mods};\n'
        sigs[name] = hashlib.sha1(body.encode('utf-8')).hexdigest()
        wrappers.append(f'model Top_{name}\n{body}end Top_{name};\n')
    wpath = os.path.join(directory, '_acausal_wrappers.mo')
    open(wpath, 'w').write('\n'.join(wrappers))
    print(f'  {wpath}')

    # connection list (only the top-level connects from the instance)
    cpath = os.path.join(directory, f'_connect_{args.model}.txt')
    open(cpath, 'w').write(
        ''.join(f'connect({a}, {b});\n' for a, b in conns))
    print(f'  {cpath}')

    # manifest consumed by the OMC orchestrator: "<name>\t<signature>" per line
    mpath = os.path.join(directory, '_acausal_components.txt')
    open(mpath, 'w').write(''.join(f'{name}\t{sigs[name]}\n' for name, _, _ in comps))
    print(f'  {mpath}')

    print(f'prepare: {len(comps)} components '
          f'[{", ".join(n for n, _, _ in comps)}], '
          f'{len(conns)} connections, {len(copies)} copied decls')


