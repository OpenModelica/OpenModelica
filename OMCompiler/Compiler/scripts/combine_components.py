"""
combine_components.py

Parse the C code generated by OpenModelica's translateResidualsDAE API and a
Modelica connection list, then produce combined C code that implements the
connect() semantics so the individual component codes can be simulated together
without re-running the full OM frontend/backend/codegen pipeline.

Background
----------
translateResidualsDAE generates a separate set of C files for each top-level
component model.  Every acausal connector variable (FluidPort m_flow, p,
h_outflow, Xi_outflow; HeatPort T, Q_flow; Real signal I/O) is exposed via
synthetic "$fdummyN" functions.  Each $fdummyN is called from a dedicated
eqFunction_K inside xxx_16dae.c, producing a DAE residual that is always 0.
These "stub" residuals are the placeholders that this tool replaces with the
real Modelica connector equations.

How the assignment works
-------------------------
A component has exactly one $fdummy residual per acausal connector.  Each such
residual is the slot for ONE connection equation contributed by that connector.

At every connection node (the set of connectors joined by connect()) the
Modelica semantics expand to:
    * one  flow-conservation equation:  sum(m_flow) = 0
    * n-1  potential-equality equations: c_i.p = c_ref.p

So one connector at each node carries the flow equation and the rest carry the
potential equalities.  The residual<->variable matching that OM used internally
is *not* emitted in the C (and is genuinely ambiguous when a component's fdummys
are structurally identical), but it is also irrelevant: any assignment that puts
each connection equation into exactly one slot yields the same solution set.

We still choose the flow owner sensibly, using the hint of which connector
variable is an algebraic DAE unknown of the component, so that each component's
residual stays well posed.

Output
------
For each component <pfx>:
    connected_<pfx>_16dae.c   - modified DAE file with real connection equations

Plus top-level files:
    combined_setup.h           - extern DATA* pointers for every component
    combined_glue.c            - DATA pointers + stream/signal propagation
    combined.makefile          - drop-in Makefile for the combined build

Usage
-----
    python3 combine_components.py \\
        --dir   /path/to/generated_files \\
        --comps sou,const,mov,res,sin \\
        --conn  _connect-example1.txt \\
        --out   combined1

    # Then build:
    make -f combined1.makefile
"""

