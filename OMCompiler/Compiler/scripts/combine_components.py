@dataclass
class GlobalJacobian:
    prefixes:   List[str]
    base:       Dict[str, int]        # component -> global row/col offset
    n:          int                   # total unknowns == total residuals
    col_rows:   List[List[int]]       # global CSC: per-column sorted row list
    color:      List[int]             # per-column color (1-based)
    n_colors:   int
    col_label:  List[str]             # per-column human-readable label
    coupling:   List[Tuple[int, int, str]]  # (row, col, note) cross-comp entries


def _reverse_var_names(comp: ComponentInfo) -> Dict[int, str]:
    rev: Dict[int, str] = {}
    for name, idx in comp.var_to_idx.items():
        rev.setdefault(idx, name)
    return rev


def _greedy_color(n: int, col_rows: List[List[int]]) -> Tuple[List[int], int]:
    """Distance-1 column coloring for a colored-FD Jacobian: two columns may
    share a color iff no residual row has nonzeros in both."""
    row_to_cols: Dict[int, List[int]] = {}
    for c, rows in enumerate(col_rows):
        for r in rows:
            row_to_cols.setdefault(r, []).append(c)

    color = [0] * n
    for c in range(n):
        used = set()
        for r in col_rows[c]:
            for other in row_to_cols[r]:
                if other != c and color[other]:
                    used.add(color[other])
        k = 1
        while k in used:
            k += 1
        color[c] = k
    return color, (max(color) if color else 0)


def build_global_jacobian(prefixes: List[str],
                          comps: Dict[str, ComponentInfo],
                          specs: Dict[Tuple[str, str], ConnEq]) -> GlobalJacobian:
    """Assemble the global DAE Jacobian sparsity from the per-component patterns
    (block diagonal) plus the cross-component coupling introduced by the
    connection equations."""
    # global layout: one block per component, size n_residuals each
    base: Dict[str, int] = {}
    off = 0
    for pfx in prefixes:
        base[pfx] = off
        off += comps[pfx].n_residuals
    n = off

    entries: Set[Tuple[int, int]] = set()
    col_label = [''] * n

    # column labels + diagonal blocks (kept as OM emitted them; the connection
    # rows only read a subset of the variables the original $fdummy depended on,
    # so the original rows remain a correct superset of the new dependencies)
    for pfx in prefixes:
        comp = comps[pfx]
        rev  = _reverse_var_names(comp)
        for j in range(comp.n_residuals):
            if j < comp.n_states:
                col_label[base[pfx] + j] = f'{pfx}: state rv{j} ({rev.get(j, "?")})'
            else:
                k = comp.alg_indexes[j - comp.n_states]
                col_label[base[pfx] + j] = f'{pfx}: alg rv{k} ({rev.get(k, "?")})'
        for j, rows in enumerate(comp.col_rows):
            for r in rows:
                entries.add((base[pfx] + r, base[pfx] + j))

    # off-diagonal coupling from the connection equations
    coupling: List[Tuple[int, int, str]] = []

    def add_dense_block(grow: int, other_pfx: str, note: str):
        oc = comps[other_pfx]
        for j in range(oc.n_residuals):
            gcol = base[other_pfx] + j
            entries.add((grow, gcol))
            coupling.append((grow, gcol, note + ' [dense: non-unknown read]'))

    for pfx in prefixes:
        comp   = comps[pfx]
        cr_map = connector_residual_map(comp)
        for connector, fr in cr_map.items():
            spec = specs.get((pfx, connector))
            if spec is None or not spec.connected:
                continue
            grow = base[pfx] + fr.residual_idx

            if spec.kind == 'flow':
                for m in spec.members:
                    if m == (pfx, connector):
                        continue          # owner's own flow is a diagonal entry
                    oc   = comps[m[0]]
                    info = connector_info(oc, m[1])
                    if info is None or info.flow_idx is None:
                        continue
                    col = oc.realvar_to_col(info.flow_idx)
                    note = (f'{pfx}.{connector} flow conservation reads '
                            f'{m[0]}.{m[1]}.{info.flow_field} (rv{info.flow_idx})')
                    if col is None:
                        add_dense_block(grow, m[0], note)
                    else:
                        gcol = base[m[0]] + col
                        entries.add((grow, gcol))
                        coupling.append((grow, gcol, note))

            else:  # potential equality reads the flow owner's potential
                owner = spec.owner
                if owner[0] == pfx:
                    continue              # reference is in the same component
                oc   = comps[owner[0]]
                info = connector_info(oc, owner[1])
                if info is None or info.pot_idx is None:
                    continue              # owner potential is a parameter -> constant
                col = oc.realvar_to_col(info.pot_idx)
                note = (f'{pfx}.{connector} potential equality reads '
                        f'{owner[0]}.{owner[1]}.{info.pot_field} (rv{info.pot_idx})')
                if col is None:
                    add_dense_block(grow, owner[0], note)
                else:
                    gcol = base[owner[0]] + col
                    entries.add((grow, gcol))
                    coupling.append((grow, gcol, note))

    col_rows: List[List[int]] = [[] for _ in range(n)]
    for (r, c) in entries:
        col_rows[c].append(r)
    for c in range(n):
        col_rows[c].sort()

    color, n_colors = _greedy_color(n, col_rows)
    return GlobalJacobian(prefixes, base, n, col_rows, color, n_colors,
                          col_label, coupling)


