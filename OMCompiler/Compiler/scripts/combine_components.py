def _realvar_expr(comp: ComponentInfo, name: str) -> Optional[str]:
    """C expression reading *name* from this component's own DATA* (`data`)."""
    idx = comp.var_to_idx.get(name)
    if idx is not None:
        return (f'(data->localData[0]->realVars'
                f'[data->simulationInfo->realVarsIndex[{idx}]]'
                f' /* {name} */)')
    pidx = comp.param_to_idx.get(name)
    if pidx is not None:
        return (f'(data->simulationInfo->realParameter'
                f'[data->simulationInfo->realParamsIndex[{pidx}]]'
                f' /* {name} PARAM */)')
    return None


def _ptr_realvar_expr(comp: ComponentInfo, name: str, ptr: str) -> Optional[str]:
    """C expression reading *name* through DATA* pointer *ptr*."""
    idx = comp.var_to_idx.get(name)
    if idx is not None:
        return (f'({ptr}->localData[0]->realVars'
                f'[{ptr}->simulationInfo->realVarsIndex[{idx}]]'
                f' /* {comp.prefix}.{name} */)')
    pidx = comp.param_to_idx.get(name)
    if pidx is not None:
        return (f'({ptr}->simulationInfo->realParameter'
                f'[{ptr}->simulationInfo->realParamsIndex[{pidx}]]'
                f' /* {comp.prefix}.{name} PARAM */)')
    return None


# ---------------------------------------------------------------------------
# Connection analysis (node based)
# ---------------------------------------------------------------------------

@dataclass
class ConnEdge:
    """A raw connect() classified as fluid (both ends are acausal connectors)
    or signal (Real input/output, handled by a value copy)."""
    comp_a:    str
    conn_a:    str
    comp_b:    str
    conn_b:    str
    is_signal: bool = False


def _connector_order(comp: ComponentInfo) -> List[str]:
    """Ordered unique connector names for *comp*, taken from a $fdummy's params.

    The count of these acausal connectors equals the number of $fdummy
    residuals; each connector contributes exactly one connection equation.
    """
    fdef = comp.fdummy_defs.get(1)
    if fdef is None and comp.fdummy_defs:
        fdef = comp.fdummy_defs[min(comp.fdummy_defs)]
    if fdef is None:
        return []
    seen: List[str] = []
    for p in fdef.params:
        if p.connector and p.connector not in seen:
            seen.append(p.connector)
    return seen


def is_fluid_connector(comp: Optional[ComponentInfo], conn: str) -> bool:
    """True if *conn* is an acausal (fluid/heat) connector exposed via fdummy."""
    return comp is not None and conn in _connector_order(comp)


def connector_residual_map(comp: ComponentInfo) -> Dict[str, FdummyResidual]:
    """Bijection connector -> fdummy residual slot.  A component's fdummys are
    structurally identical, so any stable positional mapping is valid; both the
    DAE code generator and the Jacobian assembler must use this same one."""
    connectors = _connector_order(comp)
    residuals  = sorted(comp.fdummy_residuals, key=lambda r: r.fdummy_num)
    return {connectors[i]: residuals[i]
            for i in range(min(len(connectors), len(residuals)))}


def build_edges(connections, comps) -> List[ConnEdge]:
    """Classify each raw connection as fluid or signal (for glue generation)."""
    edges = []
    for ca, pna, cb, pnb in connections:
        fa = is_fluid_connector(comps.get(ca), pna)
        fb = is_fluid_connector(comps.get(cb), pnb)
        edges.append(ConnEdge(ca, pna, cb, pnb, is_signal=not (fa and fb)))
    return edges


@dataclass
class ConnectorInfo:
    """What a component provides at one connector, plus DAE-ownership hints."""
    flow_field:  Optional[str]   # 'm_flow' | 'Q_flow'
    pot_field:   Optional[str]   # 'p' | 'T'
    flow_expr:   Optional[str]   # arg expression provided for the flow var
    pot_expr:    Optional[str]   # arg expression provided for the potential var
    flow_idx:    Optional[int]   # realVarsIndex referenced by flow_expr
    pot_idx:     Optional[int]   # realVarsIndex referenced by pot_expr (None if a parameter)
    flow_alg:    bool            # flow var is an algebraic DAE unknown
    pot_alg:     bool            # potential var is an algebraic DAE unknown
    flow_direct: bool            # flow_expr is a direct (non-negated) reference


def _representative_residual(comp: ComponentInfo):
    """Return (fr, fdef) for any fdummy residual of *comp*.  A component's
    fdummys all carry the same argument list, so any one is representative."""
    if not comp.fdummy_residuals:
        return None, None
    fr = comp.fdummy_residuals[0]
    return fr, comp.fdummy_defs.get(fr.fdummy_num)


def connector_info(comp: ComponentInfo, connector: str) -> Optional[ConnectorInfo]:
    fr, fdef = _representative_residual(comp)
    if fdef is None:
        return None

    flow_field = pot_field = None
    flow_expr  = pot_expr  = None
    for i, p in enumerate(fdef.params):
        if p.connector != connector:
            continue
        arg = fr.arg_exprs[i] if i < len(fr.arg_exprs) else None
        if p.kind == 'flow' and flow_field is None:
            flow_field, flow_expr = p.field, arg
        elif p.kind == 'potential' and pot_field is None:
            pot_field, pot_expr = p.field, arg

    flow_idx = None
    flow_alg = flow_direct = False
    if flow_expr:
        m = _RE_VARIDX.search(flow_expr)
        if m:
            flow_idx    = int(m.group(1))
            flow_alg    = flow_idx in comp.alg_indexes
            flow_direct = not flow_expr.lstrip().startswith('(-')

    pot_idx = None
    pot_alg = False
    if pot_expr:
        m = _RE_VARIDX.search(pot_expr)
        if m:
            pot_idx = int(m.group(1))
            pot_alg = pot_idx in comp.alg_indexes

    return ConnectorInfo(flow_field, pot_field, flow_expr, pot_expr,
                         flow_idx, pot_idx, flow_alg, pot_alg, flow_direct)


@dataclass
class ConnEq:
    """The connection equation assigned to one (comp, connector) fdummy slot."""
    kind:       str                       # 'flow' | 'potential'
    members:    List[Tuple[str, str]]     # all (comp, connector) at the node
    owner:      Tuple[str, str]           # flow-owner member (potential reference)
    flow_field: Optional[str]
    pot_field:  Optional[str]
    connected:  bool                      # False for a lone (unconnected) port


def build_node_specs(comps, connections) -> Dict[Tuple[str, str], ConnEq]:
    """Group fluid connectors into nodes and assign one equation per connector.

    At each node exactly one connector provides flow conservation (sum of flows
    = 0); the others provide potential equality (this.potential = owner.potential).
    The flow owner is the connector whose flow variable is the component's own
    algebraic unknown, which keeps that component's residual well posed.
    """
    parent: Dict[Tuple[str, str], Tuple[str, str]] = {}

    def find(x):
        parent.setdefault(x, x)
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(a, b):
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[rb] = ra

    members_all: List[Tuple[str, str]] = []
    for pfx in comps:
        for conn in _connector_order(comps[pfx]):
            key = (pfx, conn)
            parent.setdefault(key, key)
            members_all.append(key)

    connected_keys: Set[Tuple[str, str]] = set()
    for ca, pna, cb, pnb in connections:
        ka, kb = (ca, pna), (cb, pnb)
        if ka in parent and kb in parent:
            union(ka, kb)
            connected_keys.add(ka)
            connected_keys.add(kb)

    groups: Dict[Tuple[str, str], List[Tuple[str, str]]] = {}
    for key in members_all:
        groups.setdefault(find(key), []).append(key)

    infos = {key: connector_info(comps[key[0]], key[1]) for key in members_all}

    def flow_score(key, consumed):
        info = infos[key]
        if info is None or info.flow_field is None:
            return -1
        if (key[0], info.flow_idx) in consumed:
            return -1            # this component's flow var is already an owner
        if not info.flow_alg:
            return 0             # can host the flow eq, but it is not its unknown
        if not info.pot_alg:
            return 4             # only a flow unknown here -> strongest owner
        if info.flow_direct:
            return 3             # flow & potential unknown, flow is a direct ref
        return 2

    def n_flow_cand(members):
        return sum(1 for m in members
                   if infos[m] and infos[m].flow_alg and infos[m].flow_field)

    # Assign the most constrained nodes (fewest flow candidates) first so that a
    # 2-port's shared m_flow is consumed before its other end is considered.
    ordered = sorted(groups.values(), key=lambda ms: (n_flow_cand(ms), len(ms)))

    specs: Dict[Tuple[str, str], ConnEq] = {}
    consumed: Set[Tuple[str, Optional[int]]] = set()

    for members in ordered:
        owner = max(members,
                    key=lambda m: (flow_score(m, consumed), -members_all.index(m)))
        oinfo = infos[owner]
        if oinfo and oinfo.flow_idx is not None:
            consumed.add((owner[0], oinfo.flow_idx))

        flow_field = oinfo.flow_field if oinfo else None
        pot_field  = oinfo.pot_field  if oinfo else None
        is_conn = any(m in connected_keys for m in members)

        for m in members:
            minfo = infos[m]
            if m == owner:
                specs[m] = ConnEq('flow', members, owner,
                                  flow_field, pot_field, is_conn)
            else:
                specs[m] = ConnEq('potential', members, owner,
                                  minfo.flow_field if minfo else flow_field,
                                  minfo.pot_field  if minfo else pot_field,
                                  is_conn)
    return specs


def _member_field_expr(member, fieldname, current_prefix, comps) -> Optional[str]:
    """C expression for member.fieldname, using `data->` when *member* is the
    component currently being generated, else `g_<comp>_data->`."""
    if fieldname is None:
        return None
    comp = comps.get(member[0])
    if comp is None:
        return None

    fr, fdef = _representative_residual(comp)
    expr = None
    if fdef is not None:
        for i, p in enumerate(fdef.params):
            if p.connector == member[1] and p.field == fieldname:
                if i < len(fr.arg_exprs):
                    expr = fr.arg_exprs[i]
                break

    if expr is None:                       # connector field not in fdummy params
        name = f'{member[1]}.{fieldname}'
        if member[0] == current_prefix:
            return _realvar_expr(comp, name)
        return _ptr_realvar_expr(comp, name, f'g_{member[0]}_data')

    if member[0] == current_prefix:
        return expr
    return expr.replace('data->', f'g_{member[0]}_data->')


