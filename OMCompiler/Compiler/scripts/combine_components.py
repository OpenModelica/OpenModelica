def _parse_makefile_vars(path: str) -> Dict[str, str]:
    """Collect simple NAME=value / NAME:=value assignments from a makefile."""
    vars: Dict[str, str] = {}
    for line in open(path):
        line = line.rstrip('\n')
        if line.lstrip().startswith('#'):
            continue
        m = re.match(r'\s*([A-Za-z_][A-Za-z0-9_]*)\s*:?=\s*(.*)$', line)
        if m:
            vars[m.group(1)] = m.group(2)
    return vars


def _expand_make(value: str, vars: Dict[str, str], depth: int = 0) -> str:
    """Expand $(VAR) references using *vars* (bounded recursion)."""
    if depth > 16:
        return value
    return re.sub(r'\$\(([A-Za-z_][A-Za-z0-9_]*)\)',
                  lambda m: _expand_make(vars.get(m.group(1), ''), vars, depth + 1),
                  value)


def parse_component_link_flags(directory: str, prefixes: List[str]
                               ) -> Tuple[List[str], List[str]]:
    """Parse each <pfx>.makefile and return (include_dirs, ldflags) as the
    order-preserving union of the components' own CPPFLAGS -I dirs and their
    expanded LDFLAGS (the OM-generated, system-correct link flags).

    -Wl,--no-undefined is dropped: the combined library links only a curated
    subset of each component's objects, so some intra-component references are
    resolved lazily at load time rather than at link time."""
    inc: List[str] = []
    ld:  List[str] = []
    seen_inc: Set[str] = set()
    seen_ld:  Set[str] = set()
    drop = {'-Wl,--no-undefined'}

    for pfx in prefixes:
        mk = os.path.join(directory, f'{pfx}.makefile')
        if not os.path.exists(mk):
            continue
        v = _parse_makefile_vars(mk)
        for tok in shlex.split(_expand_make(v.get('CPPFLAGS', ''), v)):
            if tok.startswith('-I') and tok not in seen_inc:
                seen_inc.add(tok)
                inc.append(tok)
        for tok in shlex.split(_expand_make(v.get('LDFLAGS', ''), v)):
            if tok in drop or tok in seen_ld:
                continue
            seen_ld.add(tok)
            ld.append(tok)
    return inc, ld


def _quote_flag(tok: str) -> str:
    """Re-quote the path portion of -I/-L/-Wl,-rpath, flags if it contains
    characters the shell/make would choke on (spaces)."""
    for pre in ('-I', '-L', '-Wl,-rpath,'):
        if tok.startswith(pre):
            path = tok[len(pre):]
            if path and (' ' in path):
                return f'{pre}"{path}"'
    return tok


def _clean_label(lbl: str) -> str:
    """Turn a global-Jacobian column label like 'res: alg rv1 (m_flow)' into the
    real hierarchical variable name 'res.m_flow' so the combined result can be
    diffed/plotted against a normal simulation.  Unnamed states fall back to a
    unique synthetic name."""
    m = re.match(r'(.+?): (state|alg) rv(\d+) \((.*)\)$', lbl)
    if not m:
        return re.sub(r'[^A-Za-z0-9_.\[\]]', '_', lbl)
    pfx, kind, rv, name = m.group(1), m.group(2), m.group(3), m.group(4)
    if name and name != '?':
        return f'{pfx}.{name}'
    return f'{pfx}.__{kind}{rv}'


